#include "libft.h"

void	ft_disp_ar(char **ar_of_ar)
{
	int y;

	y = 0;
	while (ar_of_ar[y])
	{
		ft_putstr(ar_of_ar[y]);
		ft_putchar('\n');
		y++;
	}
}
